
public class TypeCasting {
	
	public static void main(String[] args)
	{
		int i =10;
		float f = i;
		char c = 'A';
		
		System.out.println(f);

		double d = 100.20;
		int a=(int) d;
		System.out.println(a);
		
		long l=123456789;
		short s=(short) l;
		System.out.println(s);
		
		int g = (int)c;
		System.out.println(g);
		
	}

}
